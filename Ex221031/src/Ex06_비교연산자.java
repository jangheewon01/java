
public class Ex06_비교연산자 {

	public static void main(String[] args) {
		
		int a = 3;
		int b = 10;
		
		// 비교연산자의 결과값은 항상 boolean 자료형이다
		// true or false
		
		System.out.println(a == b);
		System.out.println(a != b);
		System.out.println(a > b);
		System.out.println(a <= b); // =표시는 오른쪽에 써야 한다.

	}

}
