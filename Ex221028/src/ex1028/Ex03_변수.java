package ex1028;

public class Ex03_변수 {

	public static void main(String[] args) {

		// 해당 줄에 커서 올려놓고 Ctrl + D : 줄 삭제
		// Ctrl + Alt + 방향키(위, 아래) : 줄 복사
		// Alt + 방향키(위, 아래) : 줄 이동
		// Ctrl + Shift + F : 코드 이쁘게 정렬
		
		int a = 200;
		
		System.out.println(a + 2); // ; -> 코드 하나별로 컴파일하는데 코드를 구분해주는 구분자
		System.out.println(a - 2);
		System.out.println(a * 2);
		System.out.println(a / 2);

	}

}
